public class userHelperclass1 {

    String name11, email11, mobile11;

    public userHelperclass1() {

    }

    public userHelperclass1(String name, String email, String mobile) {
        this.name11 = name;
        this.email11 = email;
        this.mobile11 = mobile;
    }

    public String getName() {
        return name11;
    }

    public void setName(String name) {
        this.name11 = name;
    }

    public String getEmail() {
        return email11;
    }

    public void setEmail(String email) {
        this.email11 = email;
    }

    public String getMobile() {
        return mobile11;
    }

    public void setMobile(String mobile) {
        this.mobile11 = mobile;
    }
}
